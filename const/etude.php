   <h1>Etudes & Coordination</h1> 
   <img src="image/i5.jpg" style="width: 400px;">
   <p><b>BIT CONSTRUCTION</b> est doté de personnel compétent dans les études de structures et dans la coordination des chantiers.
</p>
<p>En tant que bureau d'études structure, nous réalisons la conception d'ouvrages structurels, qu'il s'agisse d'ouvrages en béton armé, en structure métallique, en structure bois ou en structure mixte.</p>
<p>Nous assistons les architectes sur le dimensionnement des ouvrages en béton. Nous dessinons des plans bétons du dossier de consultation des entreprises (DCE) et rédigons le CCTP (Cahier des Clauses Techniques Particulières) des lots Gros OEuvre (GO), Charpente bois ou métallique ou structure mixte.</p>
<p>En tant qu'économiste de la construction, nous:<br>
<br>
réalisons les quantitatifs du bâtiment;<br>
estimons les coûts de chacun des éléments constitutifs du bâtiments et des espaces extérieurs;<br>
pouvons rédiger les pièces écrites nécessaires à la consultation des entreprises.<br>
<b>En tant que bureau de coordination, BIT CONSTRUCTION assure l’ordonnancement, le pilotage et la coordination de<br> chantier et le contrôle de la conformité de l’exécution d’opération par rapport au projet.</b></p>
<p>DIRECTION DE L’EXECUTION DES CONTRATS DE TRAVAUX<br>
<br>
<b>Direction générale et coordination de chantier</b><br>
Rédaction et diffusion des comptes-rendus de chantier<br>
Gestion de la comptabilité de chantier et contrôle permanent des coûts<br>
Vérification des coûts des travaux modificatifs<br>
Décomptes généraux et définitifs</p>
<p><b>» ORDONNANCEMENT – PILOTAGE ET COORDINATION</b><br>

